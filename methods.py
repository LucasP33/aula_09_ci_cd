def area_of_rectangle(width, height):
    area = width*height
    return area
 
def perimeter_of_rectangle(width, height):
    perimeter = 2 * (width + height)
    return perimeter

def area_triangulo(windth, height):
    areat = (windth*height)/2
    return areat

def area_quadrado(windth):
    area = 2*windth
    return area

def soma(valor1, valor2):
    s = valor1 + valor2
    return soma  

def subtracao(v1, v2):
    sub = v1 - v2
    return sub

def multiplicacao(v1, v2):
    m = v1 * v2
    return m

def divisao(v1, v2):
    d = v1 / v2
